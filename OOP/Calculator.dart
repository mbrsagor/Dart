class Calculator {

// The function add two number
  void add(a, b) {
    var c = a + b;
    print(c);
  }

  // The function subscraction two number
  void sub(a, b) {
    var c = a - b;
    print(c);
  }

  // The function multification two number
  void mul(a, b) {
    var c = a * b;
    print(c);
  }

  // The function division two number
  void div(a, b) {
    var c = a / b;
    print(c);
  }


}
